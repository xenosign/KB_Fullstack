package afternoon.variable;

public class Var2Number {
    public static void main(String[] args) {
        // 정수
        byte b = 127;
        short s = 32767;
        int i = 2147483647;

        long l = 9223372036854775807L;

        // 실수
        float f = 4.3f;
        double d = 4.3;
    }
}
